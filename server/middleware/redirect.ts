export default defineEventHandler((event) => {
  const url = event.node.req.url || ''
  const match = url.match(/^\/(photo|video|blog|episode)\/([^/]+)(?:\/|$)/)
  if (!match) {
    return
  }

  const route = match[1]
  const slug = decodeURIComponent(match[2])
  const redirectsForRoute = routeRedirects[route]

  if (redirectsForRoute) {
    const targetSlug = redirectsForRoute[slug]
    if (targetSlug) {
      const destination = `/${route}/${targetSlug}`
      return sendRedirect(event, destination, 301)
    }
  }
})
